// ============================================================
// i18n Engine
// ============================================================

const JUDDEV_I18N = {
  currentLang: 'fr',

  init() {
    const saved = localStorage.getItem('juddev_lang') || 'fr';
    this.currentLang = saved;
    this.apply();
    this.updateToggle();
  },

  t(key) {
    const lang = JUDDEV_TRANSLATIONS[this.currentLang] || JUDDEV_TRANSLATIONS.fr;
    return lang[key] || JUDDEV_TRANSLATIONS.fr[key] || key;
  },

  apply() {
    // 1. Handle data-i18n attributes
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translation = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
      } else if (el.tagName === 'LABEL') {
        el.textContent = translation;
      } else {
        el.innerHTML = translation;
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });

    // 2. Translate navbar links by href
    const navMap = {
      'index.html': this.t('nav.home'),
      'a-propos.html': this.t('nav.about'),
      'services.html': this.t('nav.services'),
      'realisations.html': this.t('nav.realisations'),
      'blog.html': this.t('nav.blog'),
      'formations.html': this.t('nav.formations'),
      'contact.html': this.t('nav.contact'),
    };
    document.querySelectorAll('.navbar-links a, .navbar-mobile a').forEach(link => {
      const href = link.getAttribute('href');
      if (href && navMap[href]) {
        link.textContent = navMap[href];
      }
    });
    const devisLinks = document.querySelectorAll('.btn-devis-nav');
    devisLinks.forEach(el => el.textContent = this.t('nav.devis'));

    // 3. Translate CTA section
    const ctaBadge = document.querySelector('.cta-section .section-badge');
    if (ctaBadge) ctaBadge.textContent = this.t('section.cta.badge');
    const ctaTitle = document.querySelector('.cta-title');
    if (ctaTitle) ctaTitle.innerHTML = this.t('section.cta.title').replace('<span>', '<span class="cta-highlight">');
    const ctaSubtitle = document.querySelector('.cta-subtitle');
    if (ctaSubtitle) ctaSubtitle.textContent = this.t('section.cta.subtitle');

    // 4. Translate footer
    const footerCopyright = document.querySelector('.footer-copyright');
    if (footerCopyright) footerCopyright.textContent = this.t('footer.copyright');

    // 5. Update html lang attribute
    document.documentElement.lang = this.currentLang;

    // 6. Update date formats
    document.querySelectorAll('[data-date]').forEach(el => {
      const dateStr = el.getAttribute('data-date');
      if (dateStr) {
        const date = new Date(dateStr);
        el.textContent = date.toLocaleDateString(
          this.currentLang === 'fr' ? 'fr-FR' : 'en-US',
          { year: 'numeric', month: 'long', day: 'numeric' }
        );
      }
    });

    // 7. Update nav active lang buttons
    this.updateToggle();

    // 8. Dispatch event for dynamic content to re-render
    document.dispatchEvent(new CustomEvent('juddev:langChanged', {
      detail: { lang: this.currentLang }
    }));
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('juddev_lang', lang);
    this.apply();
  },

  updateToggle() {
    const frBtn = document.getElementById('lang-fr');
    const enBtn = document.getElementById('lang-en');
    if (frBtn) frBtn.classList.toggle('lang-active', this.currentLang === 'fr');
    if (enBtn) enBtn.classList.toggle('lang-active', this.currentLang === 'en');
  }
};

// Auto-init when DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => JUDDEV_I18N.init());
} else {
  JUDDEV_I18N.init();
}
